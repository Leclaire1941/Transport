package by.epam.classes.transport.exception;

public class TransportDataException extends Exception {




    public TransportDataException(String message, Throwable cause) {
        super(message, cause);
    }

    public TransportDataException(String message) {
        super(message);
    }

    public TransportDataException(Throwable cause) {
        super(cause);
    }

    public TransportDataException() {
        super();
    }


}
